import Foundation

/// Единый медицинский дисклеймер приложения (App Review 1.4.1: «remind users to
/// check with a doctor … before making medical decisions»). Один текст —
/// переиспользуется в онбординге и профиле, чтобы формулировка была единой и
/// проверяемой тестом.
enum Disclaimer {
    /// Полный текст для экранов с акцентом (онбординг, «О приложении»).
    /// Локализуется через String Catalog (ключ — русский текст, en-перевод в каталоге).
    static let medical = String(localized: """
    Pudding — дневник-помощник, а не медицинский совет. \
    Перед введением новых продуктов, аллергенов и любыми решениями о здоровье \
    ребёнка проконсультируйся с педиатром. При признаках аллергической реакции \
    немедленно обратись за медицинской помощью.
    """)

    /// Короткая версия для компактных мест (футер секции и т.п.).
    static let short = "Это не медицинский совет — решения принимай вместе с педиатром."

    /// Совсем короткая сноска для welcome-экрана онбординга.
    static let welcome = "Не медицинский совет — сроки согласуй с педиатром."
}
